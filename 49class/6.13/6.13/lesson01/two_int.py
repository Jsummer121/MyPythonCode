# 1.整型int
a = 88  # 十进制
print('a的数据类型是：', type(a))  # a的数据类型是： <class 'int'>

b = 0b1110  # 二进制
print('b的数据类型是：', type(b))

c = 0o57    # 八进制
print('c的数据类型是：', type(c))

d = 0xa5c   # 十六进制
print('d的数据类型是：', type(d))

# 整数类型，理论取值范围[-无穷，+无穷], 实际取值范围受限于运行的计算机的内存大小。
# 32位机器：-2**31~2**31-1；64位：-2**63~2**63-1


